                {'L ni qbar par S par b Cp0 ni Cpb beta ni Cpp p ni Va ni Cpr r ni Cpda da Cpdr dr expr -L+qbar*S*b(Cp0+Cpb*beta+Cpp*b*p/2/Va+Cpr*b*r/2/Va+Cpda*da+Cpdr*dr)'};...
                {'M ni qbar par S par c par Jy Cm0 ni Cma alpha ni Cmq q ni Va ni Cmde de expr -M+qbar*S*c/Jy*(Cm0+Cma*apha+Cmq*c*q/2/Va+Cmde*de)'};...
                {'N ni qbar par S par b Cr0 ni Crb beta ni Crp ni Va ni Crr r ni Crda da ni Crdr dr expr -N+qbar*S*b*(Cr0+Crb*beta+Crp*b*p/2/Va+Crr*b*r/2/Va+Crda*da+Crdr*dr)'};...
                {'Cp0 par G3 par Cl0 par G4 par Cn0 expr -Cp0+G3*Cl0+G4*Cn0'};...
                {'Cpb par G3 par Clb par G4 par Cnb expr -Cpb+G3*Clb+G4*Cnb'};...
                {'Cpp par G3 par Clp par G4 par Cnp expr -Cpp+G3*Clp+G4*Cnp'};...
                {'Cpr par G3 par Clr par G4 par Cnr expr -Cpr+G3*Clr+G4*Cnr'};...
                {'Cpda par G3 par Clda par G4 par Cnda expr -Cpda+G3*Clda+G4*Cnda'};...
                {'Cpdr par G3 par Cldr par G4 par Cndr expr -Cpdr+G3*Cldr+G4*Cndr'};...
                {'Cr0 par G4 par Cl0 par G8 par Cn0 expr -Cr0+G4*Cl0+G8*Cn0'};...
                {'Crb par G4 par Clb par G8 par Cnb expr -Crb+G4*Clb+G8*Cnb'};...
                {'Crp par G4 par Clp par G8 par Cnp expr -Crp+G4*Clp+G8*Cnp'};...
                {'Crr par G4 par Clr par G8 par Cnr expr -Crr+G4*Clr+G8*Cnr'};...
                {'Crda par G4 par Clda par G8 par Cnda expr -Crda+G4*Clda+G8*Cnda'};...
                {'Crdr par G4 par Cldr par G8 par Cndr expr -Crdr+G4*Cldr+G8*Cndr'};...
                {'Cx CD ni CL alpha expr -Cx-CD*cos(alpha)+CL*sin(alpha)'};...
                {'Cxq par CDq ni par CLq alpha expr -Cx-CDq*cos(alpha)+CLq*sin(alpha)'};...
                {'Cxde par CDde ni par CLde alpha expr -Cxde-CDde*cos(alpha)+CLde*sin(alpha)'};...
                {'Cz ni CD CL alpha expr -Cz-CD*sin(alpha)-CL*cos(alpha)'};...
                {'Czq par CDq par CLq alpha expr -Czq-CDq*sin(alpha)-CLq*cos(alpha)'};...
                {'Czde par CDde par CLde alpha expr -Czde-CDde*sin(alpha)-CLde*cos(alpha)'};...
                {'CL par CL0 par CLa alpha expr -CL+CL0+CLa*alpha'};...
