            der = [...
                {'dot pn int dot_pn'};...
                {'dot pe int dot_pe'};...
                {'dot h int dot_h'};...
                {'dot u int dot_u'};...
                {'dot v int dot_v'};...
                {'dot w int dot_w'};...
                {'dot phi int dot_phi'};...
