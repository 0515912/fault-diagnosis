            % L_0, M_0, R_star
            
            %%
            kin = [...
                {'dot_pn ni u ni v ni w ni phi ni theta ni psi expr -dot_pn+(cos(theta)*cos(psi))*u+(sin(phi)*sin(theta)*cos(psi)-cos(phi)*sin(psi))*v+(cos(phi)*sin(theta)*cos(psi)+sin(phi)*sin(psi))*w'};...
